const Addition = (left: number, right: number): number => {
  return left + right;
};
const Subtract = (left: number, right: number): number => {
  return left - right;
};
const Multiply = (left: number, right: number): number => {
  return left * right;
};
const Divide = (left: number, right: number): number => {
  return left / right;
};
export { Addition, Subtract, Multiply, Divide };
